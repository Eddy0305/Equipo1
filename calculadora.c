#include<stdio.h>
#include<stdlib.h>
#include<math.h>

double potencia(float num1, float potencia){
  double resultado = (double)pow(num1, potencia);
  return resultado; 
}

float suma(float num1, float num2)
{
  float total = 0;
  
  total = num1 + num2;


  return total;
}
float resta(float num1, float num2)
{
  float total = 0;
  
  total = num1 - num2;


  return total;
}
